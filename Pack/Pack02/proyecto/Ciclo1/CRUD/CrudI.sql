	PROCEDURE AD_PERTENECE_EQU (xID_USUARIO IN VARCHAR,xID_EQUIPO IN NUMBER) IS
		BEGIN
		INSERT INTO PERTENECE_EQUIPO (ID_USUARIO,ID_EQUIPO) VALUES (xID_USUARIO,xID_EQUIPO) ;
		COMMIT;
		EXCEPTION
		WHEN OTHERS THEN
		ROLLBACK;
